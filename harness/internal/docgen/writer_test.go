func TestDeriveKey(t *testing.T) {
	cases := []struct {
		filename string
		want     string
	}{
		{"AmityCommunityMembershipAddMembers.kt", "community-membership-add-members"},
		{"AmityPostCreateTextPost.swift", "post-create-text-post"},
		{"AmityChannelGet.dart", "channel-get"},
		{"AmityUserGetByIds.ts", "user-get-by-ids"},
		{"AmityStoryGetActiveStories.dart", "story-get-active-stories"},
	}
	for _, tc := range cases {
		t.Run(tc.filename, func(t *testing.T) {
			assert.Equal(t, tc.want, docgen.DeriveKey(tc.filename))
		})
	}
}

